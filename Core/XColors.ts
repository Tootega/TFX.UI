    private static _HighHue: XArray<string> = null;
    static get HighHue(): XArray<string>
    {
        if (XColors._HighHue == null)
        {
            var hsl = XColors.Colors.Select<XHSLColor>(c => this.Convert(c)).Where(c => c.H > 0.3 && c.L < 0.7);
            XColors._HighHue = hsl.Select<string>(c => c.RGB);
        }
        return XColors._HighHue;
    }
    static RColor(): string
    {
        let r = Math.floor(Math.random() * (this.Colors.length - 1));
        return this.Colors[r];
    }
    static Convert(pColor: string): XHSLColor
    {
        var rgb = XHSLColor.StringToRGB(pColor);
        return XHSLColor.RGBToHSL(rgb[0], rgb[1], rgb[2]);
    }
